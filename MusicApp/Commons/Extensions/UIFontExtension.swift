import UIKit

extension UIFont {
  
  static var fontBold25: UIFont {
    return UIFont(name: "OpenSans-Bold", size: 25)!
  }
  
  static var fontBold15: UIFont {
    return UIFont(name: "OpenSans-Bold", size: 15)!
  }
  
  static var fontBold10: UIFont {
    return UIFont(name: "OpenSans-Bold", size: 10)!
  }
  
  static var fontRegular12: UIFont {
    return UIFont(name: "OpenSans-Regular", size: 12)!
  }
  
}

